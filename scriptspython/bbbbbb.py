A = float(input())
B = float(input())
C = float(input()) 
tria = (A*C)/2
circ = 3.14159 * C**2
trap = (A+B)*C/2
quad = B*B
reta = A*B
print('Triangulo: {:.3f}'.format(tria))
print('circulo: {:.3f}'.format(circ))
print('trapezio: {:.3f}'.format(trap))
print('quadrado: {:.3f}'.format(quad))
print('retangulo: {:.3f}'.format(reta))


