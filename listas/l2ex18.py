n = int(input())
if n == 1:
    print('Segunda-feira')
if n == 2:
    print('Terça-feira')
if n == 3:
    print('Quarta-feira')
if n == 4:
    print('Quinta-feira')
if n == 5:
    print('Sexta-feira')
if n == 6:
    print('Sabado')
if n == 7:
    print('Domingo')
else:
    print('Dia da semana inválido')
